package cn.epi.port.entity;

import cn.epi.sys.entity.DataEntity;

public class PortEntity extends DataEntity<PortEntity> {

	private static final long serialVersionUID = 1L;

	private String portName; // 接口名称
	private String userName; // 创建人
	private String commitSql; // sql语句
	private String sourceId; // 数据源id
	private String portComment; // 接口说明
	private String params;//SQL参数
	private String state;//接口类型
	private String firstjavaname;//第一个java名称
	private String firstjava;//第一个java
	private String lastjavaname;//第二个java名称
	private String lastjava;//第二个java
	
	
	public String getPortName() {
		return portName;
	}

	public void setPortName(String portName) {
		this.portName = portName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getCommitSql() {
		return commitSql;
	}

	public void setCommitSql(String commitSql) {
		this.commitSql = commitSql;
	}

	public String getSourceId() {
		return sourceId;
	}

	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}

	public String getPortComment() {
		return portComment;
	}

	public void setPortComment(String portComment) {
		this.portComment = portComment;
	}

	public String getParams() {
		return params;
	}

	public void setParams(String params) {
		this.params = params;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getFirstjavaname() {
		return firstjavaname;
	}

	public void setFirstjavaname(String firstjavaname) {
		this.firstjavaname = firstjavaname;
	}

	public String getFirstjava() {
		return firstjava;
	}

	public void setFirstjava(String firstjava) {
		this.firstjava = firstjava;
	}

	public String getLastjavaname() {
		return lastjavaname;
	}

	public void setLastjavaname(String lastjavaname) {
		this.lastjavaname = lastjavaname;
	}

	public String getLastjava() {
		return lastjava;
	}

	public void setLastjava(String lastjava) {
		this.lastjava = lastjava;
	}
	
	
}
